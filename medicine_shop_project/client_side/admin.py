from django.contrib import admin

from .models import Order, Medicine


@admin.register(Order)
class OrderAdmin(admin.ModelAdmin):
    list_display = ('receive_date_time', 'cost', 'complete')
    list_filter = ('receive_date_time', 'cost', 'complete')
    search_fields = ('receive_date_time', 'cost', 'complete')
    # form = OrderForm


@admin.register(Medicine)
class MedicineAdmin(admin.ModelAdmin):
    list_display = ('trade_name', 'price', 'manufacturer')
    list_filter = ('trade_name', 'price', 'manufacturer')
    search_fields = ('trade_name', 'price', 'manufacturer')
    # form = MedicineForm
